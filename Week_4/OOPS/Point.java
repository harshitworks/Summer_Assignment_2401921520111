package Week_4.OOPS;

public class Point {
    private int x,y;

    public Point() {
        x = 1 ;
        y = 1 ;
    }
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }
    public void setX(int x) {
        this.x = x;
    }
    public void setY(int y) {
        this.y = y;
    }
    public void setXY(int x ,int y) {
        this.x = x;
        this.y = y;
    }


}
